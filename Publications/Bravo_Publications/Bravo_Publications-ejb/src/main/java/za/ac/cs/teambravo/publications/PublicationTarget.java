package za.ac.cs.teambravo.publications;


import java.net.URL;


public class PublicationTarget {
    String name;
    URL website;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public URL getWebsite() {
        return website;
    }

    public void setWebsite(URL website) {
        this.website = website;
    }
}
